/**
 * op_add - operate a and b
 * @a: int
 * @b: int
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - operate a and b
 * @a: int
 * @b: int
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - operate a and b
 * @a: int
 * @b: int
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}


/**
 * op_div - operate a and b
 * @a: int
 * @b: int
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - operate a and b
 * @a: int
 * @b: int
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
